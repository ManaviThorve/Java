import java.util.Scanner;
class Add
{
	public static void main(String args[])
		{
			int a,b,add;
			Scanner sc=new Scanner(System.in);
			System.out.println("Enter the Value for a & b : ");
			a=sc.nextInt();
			b=sc.nextInt();
			add=a+b;
			System.out.println("Addition is : "+add);
			
		}
}
